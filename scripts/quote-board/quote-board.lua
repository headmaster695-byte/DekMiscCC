-- ============================================================
-- Shared state (safe: CC:T cooperative multitasking means only
-- one coroutine executes at a time — no data races)
-- ============================================================
local allQuotes  = {}
local chatQuotes = {}
local state = {
  songName     = "—",
  quoteIdx     = 1,
  chatCount    = 0,
  speakerCount = 0,   -- updated by main() after speaker scan
}

-- ============================================================
-- Persistent chat-quote storage
-- ============================================================
local function loadChatQuotes()
  if not fs.exists(CHAT_FILE) then return end
  local f = fs.open(CHAT_FILE, "r")
  if not f then return end
  local raw = f.readAll()
  f.close()
  local ok, data = pcall(textutils.unserialise, raw)
  if ok and type(data) == "table" then
    chatQuotes = data
  end
end

local function saveChatQuotes()
  local f = fs.open(CHAT_FILE, "w")
  if not f then return end
  f.write(textutils.serialise(chatQuotes))
  f.close()
end

local function rebuildAllQuotes()
  allQuotes = {}
  for _, q in ipairs(BUILTIN_QUOTES) do
    table.insert(allQuotes, q)
  end
  for _, q in ipairs(chatQuotes) do
    table.insert(allQuotes, q)
  end
  state.chatCount = #chatQuotes
  if state.quoteIdx > #allQuotes then
    state.quoteIdx = 1
  end
end

local function addChatQuote(user, msg)
  if #chatQuotes >= MAX_CHAT_QUOTES then
    table.remove(chatQuotes, 1)
  end
  table.insert(chatQuotes, { msg, user, "PLAYER" })
  saveChatQuotes()
  rebuildAllQuotes()
end

-- ============================================================
-- Display helpers
-- ============================================================
local CATEGORY_COLOUR = {
  TIP              = colours.yellow,
  ["DID YOU KNOW"] = colours.cyan,
  WISDOM           = colours.lime,
  LOADING          = colours.lightGrey,
  QUOTE            = colours.orange,
  PLAYER           = colours.pink,
}

local function wordWrap(text, width)
  local lines, line = {}, ""
  for word in text:gmatch("%S+") do
    if line == "" then
      line = word
    elseif #line + 1 + #word <= width then
      line = line .. " " .. word
    else
      table.insert(lines, line)
      line = word
    end
  end
  if line ~= "" then table.insert(lines, line) end
  return lines
end

local function centre(text, width)
  local pad = math.max(0, math.floor((width - #text) / 2))
  return string.rep(" ", pad) .. text
end

-- ============================================================
-- Monitor rendering
-- ============================================================
local function drawScreen(mon, quote)
  local w, h = mon.getSize()

  local text     = quote[1]
  local source   = quote[2]
  local category = quote[3]

  -- Clear with black background
  mon.setBackgroundColour(colours.black)
  mon.setTextColour(colours.white)
  mon.clear()

  -- Row 1: header bar (dark-grey bg, yellow text)
  mon.setCursorPos(1, 1)
  mon.setBackgroundColour(colours.grey)
  mon.setTextColour(colours.yellow)
  mon.clearLine()
  mon.setCursorPos(1, 1)
  mon.write(centre("* MOTIVATIONAL CORNER *", w))

  mon.setBackgroundColour(colours.black)

  -- Row 2: divider
  mon.setTextColour(colours.grey)
  mon.setCursorPos(1, 2)
  mon.write(string.rep("-", w))

  -- Row 3: category badge
  local catCol = CATEGORY_COLOUR[category] or colours.white
  mon.setTextColour(catCol)
  mon.setCursorPos(2, 3)
  mon.write("[ " .. category .. " ]")

  -- Rows 5+: quote body (word-wrapped, 2-char margin each side)
  mon.setTextColour(colours.white)
  local margin   = 3
  local bodyW    = w - margin * 2
  local wrapped  = wordWrap(text, bodyW)

  -- Decorate with curly quotes
  if #wrapped >= 1 then
    wrapped[1]       = "\xE2\x80\x9C" .. wrapped[1]
    wrapped[#wrapped] = wrapped[#wrapped] .. "\xE2\x80\x9D"
  end

  local bodyStart = 5
  -- Reserve last 3 rows for attribution + divider + music bar
  local bodyEnd   = h - 3
  for i, line in ipairs(wrapped) do
    local row = bodyStart + i - 1
    if row > bodyEnd then break end
    mon.setCursorPos(margin, row)
    mon.write(line:sub(1, bodyW))
  end

  -- Attribution (right-aligned, two rows above music bar)
  mon.setTextColour(colours.lightGrey)
  local attr    = "— " .. source
  local attrRow = math.min(bodyStart + #wrapped + 1, h - 2)
  attrRow = math.max(attrRow, bodyStart + 1)
  if attrRow < h - 1 then
    mon.setCursorPos(math.max(1, w - #attr - 1), attrRow)
    mon.write(attr:sub(1, w - 2))
  end

  -- Divider above music bar
  mon.setTextColour(colours.grey)
  mon.setCursorPos(1, h - 1)
  mon.write(string.rep("-", w))

  -- Last row: music bar (dark-grey bg, green text)
  mon.setCursorPos(1, h)
  mon.setBackgroundColour(colours.grey)
  mon.setTextColour(colours.green)
  mon.clearLine()
  local chatLabel = state.chatCount > 0
    and (" | chat: " .. state.chatCount .. " quote" .. (state.chatCount == 1 and "" or "s"))
    or ""
  local spkLabel  = state.speakerCount > 1
    and (" | " .. state.speakerCount .. " spkrs")
    or ""
  local musicLine = "  * " .. state.songName .. spkLabel .. chatLabel
  mon.setCursorPos(1, h)
  mon.write(musicLine:sub(1, w))

  mon.setBackgroundColour(colours.black)
  mon.setTextColour(colours.white)
end

-- ============================================================
-- Display loop — rotates quotes every QUOTE_INTERVAL seconds
-- ============================================================
local function displayLoop(mon)
  while true do
    local q = allQuotes[state.quoteIdx]
    if q then
      drawScreen(mon, q)
    end
    os.sleep(QUOTE_INTERVAL)
    state.quoteIdx = (state.quoteIdx % #allQuotes) + 1
  end
end

-- ============================================================
-- Speaker discovery
-- Scans every attached peripheral (including wired-modem remotes)
-- for the "speaker" type. Returns a table of wrapped peripherals.
-- ============================================================
local function findAllSpeakers()
  local found = {}
  for _, name in ipairs(peripheral.getNames()) do
    if peripheral.getType(name) == "speaker" then
      table.insert(found, peripheral.wrap(name))
    end
  end
  -- Fallback: peripheral.find() as a safety net in case the type
  -- string differs slightly in some CC:T builds
  if #found == 0 then
    local spk = peripheral.find("speaker")
    if spk then table.insert(found, spk) end
  end
  return found
end

-- ============================================================
-- Polyphonic song helpers
-- ============================================================

-- Normalise song format: both single-voice (`notes`) and multi-voice
-- (`voices`) songs return a list of voice tables { notes = {...} }.
local function getSongVoices(song)
  if song.voices then return song.voices end
  if song.notes  then return { { notes = song.notes } } end
  return {}
end

-- Play all voices of a song in parallel, distributing them across the
-- available speakers in round-robin order.
--   1 speaker  → all voices share it (notes interleave cooperatively)
--   2 speakers → voice 1 on spk 1, voice 2 on spk 2, voice 3 on spk 1 …
--   N speakers → ideal when #speakers == #voices
-- The function returns when the longest voice finishes.
local function playSong(song, speakers)
  local voices = getSongVoices(song)
  if #voices == 0 or #speakers == 0 then return end

  local tasks = {}
  for i, voice in ipairs(voices) do
    local spk   = speakers[(i - 1) % #speakers + 1]
    local notes = voice.notes
    tasks[#tasks + 1] = function()
      for _, note in ipairs(notes) do
        local ok = spk.playNote(note[1], note[2], note[3])
        if not ok then
          os.sleep(0.05)
          spk.playNote(note[1], note[2], note[3])
        end
        os.sleep(note[4])
      end
    end
  end

  parallel.waitForAll(table.unpack(tasks))
end

-- ============================================================
-- Music loop — cycles through SONGS indefinitely
-- ============================================================
-- speakers: table of wrapped speaker peripherals (may be empty)
-- SONGS[1] always plays first; all subsequent picks are random with
-- no back-to-back repeats.
local function musicLoop(speakers)
  if #speakers == 0 then
    while true do os.sleep(60) end
  end

  local isFirst = true
  local lastIdx = 0

  while true do
    local idx
    if isFirst then
      idx      = 1
      isFirst  = false
    else
      repeat
        idx = math.random(#SONGS)
      until idx ~= lastIdx
    end
    lastIdx = idx

    local song = SONGS[idx]
    state.songName = song.name

    playSong(song, speakers)
  end
end

-- ============================================================
-- Chat Box detection
-- Advanced Peripherals has shipped the Chat Box under several
-- different peripheral type strings across versions. We try them
-- all, then fall back to a substring scan of all attached peripherals.
-- The user can also pin a specific side/name via CHAT_BOX_NAME.
-- ============================================================
local CHAT_EVENT_NAMES = { "chat", "chat_message" }  -- AP has used both

local function findChatBox()
  if CHAT_BOX_NAME ~= "" then
    return peripheral.wrap(CHAT_BOX_NAME)
  end
  for _, typeName in ipairs({ "chatBox", "chat_box", "chatbox" }) do
    local p = peripheral.find(typeName)
    if p then return p end
  end
  -- Last resort: scan every attached peripheral for "chat" in the type name
  for _, name in ipairs(peripheral.getNames()) do
    local t = peripheral.getType(name)
    if type(t) == "string" and t:lower():find("chat", 1, true) then
      return peripheral.wrap(name)
    end
  end
  return nil
end

-- ============================================================
-- Chat capture loop (requires Advanced Peripherals Chat Box)
-- Listens for both "chat" and "chat_message" events so the script
-- works across different Advanced Peripherals version strings.
-- ============================================================
local function isChatEvent(name)
  for _, n in ipairs(CHAT_EVENT_NAMES) do
    if n == name then return true end
  end
  return false
end

local function chatLoop()
  local box = findChatBox()
  if not box then
    while true do os.sleep(60) end
  end

  while true do
    -- No event filter: we wake on every event and check the name ourselves
    -- so we catch whichever string Advanced Peripherals happens to fire.
    local ev = {os.pullEvent()}
    if isChatEvent(ev[1]) then
      local user = ev[2]
      local msg  = ev[3]
      if type(user) == "string" and type(msg) == "string"
        and #msg >= MIN_MSG_LEN
        and msg:sub(1, 1) ~= "/"
        and msg:sub(1, 1) ~= "!" then
        addChatQuote(user, msg)
      end
    end
  end
end

-- ============================================================
-- Entry point
-- ============================================================
local function main()
  loadChatQuotes()
  rebuildAllQuotes()
  math.randomseed(os.epoch("utc"))
  state.quoteIdx = math.random(#allQuotes)

  -- ── Peripheral diagnostics ────────────────────────────────
  local attached = peripheral.getNames()
  print("quote-board: attached peripherals (" .. #attached .. ")")
  for _, name in ipairs(attached) do
    print("  " .. name .. " -> " .. tostring(peripheral.getType(name)))
  end
  if #attached == 0 then
    print("  (none)")
  end
  print("")

  -- ── Monitor (required) ────────────────────────────────────
  local mon = peripheral.find("monitor")
  assert(mon, "No monitor found. Attach an Advanced Monitor and reboot.")
  assert(mon.isColour(), "Monitor must be an Advanced (colour) Monitor.")
  mon.setTextScale(MONITOR_SCALE)

  -- ── Speakers (optional) — scan for all connected speakers ─
  local speakers = findAllSpeakers()
  state.speakerCount = #speakers

  if #speakers == 0 then
    print("[WARN] No speakers found — music disabled.")
    print("       Attach speakers directly or via wired modem.")
  elseif #speakers == 1 then
    print("[OK]  1 speaker found (monophonic).")
  else
    print("[OK]  " .. #speakers .. " speakers found — polyphonic enabled.")
    print("       Songs with multiple voices will use " .. #speakers .. " channels.")
  end

  -- ── Chat Box (optional, requires Advanced Peripherals) ────
  local box = findChatBox()
  if not box then
    print("[WARN] No Chat Box found — chat capture disabled.")
    print("       Tried types: chatBox, chat_box, chatbox")
    if CHAT_BOX_NAME ~= "" then
      print("       Also tried CHAT_BOX_NAME = '" .. CHAT_BOX_NAME .. "'")
    else
      print("       Set CHAT_BOX_NAME in config to specify a side/name manually.")
    end
  else
    print("[OK]  Chat Box detected.")
  end

  local hasWarnings = #speakers == 0 or not box
  if hasWarnings then
    os.sleep(3)
  end

  parallel.waitForAll(
    function() displayLoop(mon) end,
    function() musicLoop(speakers) end,
    function() chatLoop() end
  )
end

-- Ensure terminal is always restored on exit (including Ctrl+T)
local ok, err = pcall(main)

-- Stop every speaker that was playing
for _, pName in ipairs(peripheral.getNames()) do
  if peripheral.getType(pName) == "speaker" then
    local s = peripheral.wrap(pName)
    if s then s.stop() end
  end
end

term.setBackgroundColour(colours.black)
term.setTextColour(colours.white)
term.clear()
term.setCursorPos(1, 1)

if not ok and err ~= "Terminated" then
  printError("quote-board error: " .. tostring(err))
else
  print("Quote Board stopped.")
end
